package com.tka.dec16;

public class Operator {
	
	public static void main(String[] args) {
		
		int a; 
		a = 45;
		System.out.println(a);
		
		int t;
		t = 4;
		t += 10;
		System.out.println(t);
		t -= 10;
		System.out.println(t);
		t *= 10;
		System.out.println(t);
		t /= 10;
		System.out.println(t);
		t %= 4;
		System.out.println(t);
		
		
	}
}
